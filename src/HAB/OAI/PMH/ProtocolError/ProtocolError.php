namespace HAB\OAI\PMH\ProtocolError;

use RuntimeException;

/**
 * Base class of OAI-PMH 2.0 protocol errors.
 *
 * @author    David Maus <maus@hab.de>
 * @copyright (c) 2016 by Herzog August Bibliothek Wolfenbüttel
 * @license   http://www.gnu.org/licenses/gpl.txt GNU General Public License v3 or higher
 */
abstract class ProtocolError extends RuntimeException
{
    /**
     * Return error code.
     *
     * @return string
     */
    final public function getErrorCode ()
    {
        return lcfirst(substr(static::class, 1 + strrpos(static::class, '\\')));
    }
}